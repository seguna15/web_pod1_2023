//preincrement
let myNum = 5;
console.log(`preincrementing ${myNum}: ${++myNum}`);

//predecrement 
let myVal = 5;
console.log(`predecrementing ${myVal}: ${--myVal}`);

//postincrement
let myNum2 = 5;
console.log(`postincrementing ${myNum2}: ${myNum2++}`);
console.log(`line after postincrementing: ${myNum2++}`);
//postdecrement 
let myVal2 = 5;
console.log(`postdecrementing ${myVal2}: ${myVal2--}`);
console.log(`line after postdecrementing: ${myVal2++}`);