//Assignment Operators

// =
let myNum = 10;
console.log(`myNum: ${myNum}`);

//+= addition and self assignment
let mySum = 10;
mySum += 2; // mySum = mySum + 2;
console.log(`mySum: ${mySum}`);

//-= Subtraction and self assignment
let mySub = 10;
mySub -= 2; // mySub = mySub - 2;
console.log(`mySub: ${mySub}`);

//*= Multiplication and self assignment
let myMult = 10;
myMult *= 2; // myMult = myMult * 2;
console.log(`myMult: ${myMult}`);

// /= Division and self assignment
let myDiv = 10;
myDiv /= 2; // myDiv = myDiv / 2;
console.log(`myDiv: ${myDiv}`);