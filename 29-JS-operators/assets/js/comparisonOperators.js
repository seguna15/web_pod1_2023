let num1 = 10;
let num2 = 5;
let myStr = '5';
//Greater than
console.log(`Is ${num1} greater than ${num2}: ${num1 > num2}`);
//Less than
console.log(`Is ${num1} less than ${num2}: ${num1 < num2}`);
//Greater than or equal to
console.log(`Is ${num1} greater than or equal to ${num2}: ${num1 >= num2}`);
//Less than or equal to
console.log(`Is ${num1} less than or equal to ${num2}: ${num1 <= num2}`);
//not equal to
console.log(`Is ${num2} not equal to ${myStr}: ${num2 != myStr}`);
//strict not equal to
console.log(`Is ${num2} strictly not equal to '${myStr}': ${num2 !== myStr}`);
//equal to
console.log(`Is ${num2} equal to '${myStr}': ${num2 == myStr}`);
//strict equal to
console.log(`Is ${num2} strictly equal to '${myStr}': ${num2 === myStr}`);
console.log(`typeof ${num2}: ${typeof num2}`);
console.log(`typeof '${myStr}': ${typeof myStr}`);